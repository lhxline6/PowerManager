package test.Droidlogin.library;

import java.util.Map;

public class News {
	private String name;
	private String state;
	private Map icon;
	private int pic;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getState() {
		return state;
	}
	public void setState(String ct_id) {
		this.state = ct_id;
	}
	public Map getIcon() {
		return icon;
	}
	public void setIcon(Map icon) {
		this.icon = icon;
	}
	public int getPic() {
		return pic;
	}
	public void setPic(int pic) {
		this.pic = pic;
	}
	
}
